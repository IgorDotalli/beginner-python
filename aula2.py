"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro das aspas
"""
# Aspas simples
print('Igor Dotalli Mendes')
print(1, 'Igor "Dotalli" Mendes')

# Aspas duplas
print("Igor Dotalli Mendes")
print(2, "Igor 'Dotalli' Mendes")

# Escape
print("Igor \"Dotalli \"Mendes")

# r
print(r"Igor \"Dotalli \"Mendes")
